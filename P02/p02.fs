let score_rps p =
    match p with
    | "X" -> 1
    | "Y" -> 2
    | "Z" -> 3
    | _ -> 0

let outcome (p1, p2) =
    score_rps p2
    + match p1 with
      | "A" ->
          match p2 with
          | "X" -> 3
          | "Y" -> 6
          | "Z" -> 0
          | _ -> 0
      | "B" ->
          match p2 with
          | "X" -> 0
          | "Y" -> 3
          | "Z" -> 6
          | _ -> 0
      | "C" ->
          match p2 with
          | "X" -> 6
          | "Y" -> 0
          | "Z" -> 3
          | _ -> 0
      | _ -> 0

let score (p, o) =
    match o with
    | "X" ->
        0
        + match p with
          | "A" -> 3
          | "B" -> 1
          | "C" -> 2
          | _ -> 0
    | "Y" ->
        3
        + match p with
          | "A" -> 1
          | "B" -> 2
          | "C" -> 3
          | _ -> 0
    | "Z" ->
        6
        + match p with
          | "A" -> 2
          | "B" -> 3
          | "C" -> 1
          | _ -> 0
    | _ -> 0

let simulate_rps (input: string) =
    input.Trim().Split("\r\n")
    |> List.ofArray
    |> List.map (fun s -> s.Split(" "))
    |> List.map (fun s -> s[0], s[1])
    |> List.map outcome
    |> List.sum

let simulate_strategy (input: string) =
    input.Trim().Split("\r\n")
    |> List.ofArray
    |> List.map (fun s -> s.Split(" "))
    |> List.map (fun s -> s[0], s[1])
    |> List.map score
    |> List.sum
